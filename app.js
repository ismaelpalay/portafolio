let currentLang = localStorage.getItem('lang') || 'en';

function applyTranslations(lang) {
  const t = translations[lang];
  document.querySelectorAll('[data-i18n]').forEach(el => {
    if (t[el.dataset.i18n] !== undefined) el.textContent = t[el.dataset.i18n];
  });
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    if (t[el.dataset.i18nHtml] !== undefined) el.innerHTML = t[el.dataset.i18nHtml];
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    if (t[el.dataset.i18nPlaceholder] !== undefined) el.placeholder = t[el.dataset.i18nPlaceholder];
  });
  document.documentElement.lang = lang;
  document.getElementById('langToggle').textContent = lang === 'en' ? 'ES' : 'EN';
  localStorage.setItem('lang', lang);
  currentLang = lang;

  /* update dynamic contact hrefs */
  const wa = encodeURIComponent(t['msg-wa']);
  const sms = encodeURIComponent(t['msg-imessage']);
  const mailSubj = encodeURIComponent(t['msg-email-subject']);
  const mailBody = encodeURIComponent(t['msg-email-body']);
  const waUrl = `https://wa.me/13463730309?text=${wa}`;
  const smsUrl = `sms:+13463730309&body=${sms}`;
  const mailUrl = `mailto:iismaelmadrigal.46@gmail.com?subject=${mailSubj}&body=${mailBody}`;

  ['badgeWa', 'contactWa'].forEach(id => { const el = document.getElementById(id); if (el) el.href = waUrl; });
  ['badgeMsg', 'contactMsg'].forEach(id => { const el = document.getElementById(id); if (el) el.href = smsUrl; });
  ['badgeMail', 'contactEmail'].forEach(id => { const el = document.getElementById(id); if (el) el.href = mailUrl; });
}

document.getElementById('langToggle').addEventListener('click', () => {
  applyTranslations(currentLang === 'en' ? 'es' : 'en');
});

applyTranslations(currentLang);

